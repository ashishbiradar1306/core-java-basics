package operators;

public class AssignmentOperator {
    public static void main(String [] args){

        // Assignment Operator Is Used To :
        // 1 . Assigning a constant value
        // 2 . Assigning one variable to another

        int a = 10;
        System.out.println(a);
        int b = a;
        System.out.println(b);

    }
}
